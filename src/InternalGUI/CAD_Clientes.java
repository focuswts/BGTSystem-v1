    public void Cadastrar(){
        try{
String nome = this.txt_Nome.getText();
String telefone = this.Ftxt_Telefone.getText();
String email = this.txt_Email.getText();
String endereco = this.txt_Endereco.getText();
String cpf = this.Ftxt_Cpf.getText();

String sql = "INSERT INTO clientes (nomeCliente,telCliente,emailCliente,endereco,cpfCliente) values (?,?,?,?,?)";
        PreparedStatement stt = con.prepareStatement(sql);
stt.setString(1, nome);
stt.setString(2, telefone);
stt.setString(3, email);
stt.setString(4, endereco);
stt.setString(5, cpf);
stt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cadastro Efetuado Com Sucesso!");
            stt.close();
            this.dispose();
        }catch(SQLException e){
         JOptionPane.showMessageDialog(this, "Ocorreu Algum Erro Durante O Cadastro!");
        }
    }//FIM CADASTRAR//
    
    
    

