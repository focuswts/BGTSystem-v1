public class sellItemsClass {
    private String codProduto;
    private String desc_Produto;
    private String qtd_Produto;
    private String val_Unitario;
    private boolean enabled;

    public String getCodProduto() {
        return codProduto;
    }

    public void setCodProduto(String codProduto) {
        this.codProduto = codProduto;
    }

    public String getDesc_Produto() {
        return desc_Produto;
    }

    public void setDesc_Produto(String desc_Produto) {
        this.desc_Produto = desc_Produto;
    }

    public String getQtd_Produto() {
        return qtd_Produto;
    }

    public void setQtd_Produto(String qtd_Produto) {
        this.qtd_Produto = qtd_Produto;
    }

    public String getVal_Unitario() {
        return val_Unitario;
    }

    public void setVal_Unitario(String val_Unitario) {
        this.val_Unitario = val_Unitario;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = true;
    }
    
    
    
    
    
}
