    private void preenche_itemsVenda() {
        DefaultTableModel aModel = (DefaultTableModel) this.tb_itemsVenda.getModel();
        int idVenda = Integer.valueOf(tb_Vendas.getValueAt(tb_Vendas.getSelectedRow(), 0).toString());
        try {
            int numlinhas = aModel.getRowCount();
            for (int i = 0; i < numlinhas; i++) {
                aModel.removeRow(0);
            }
            String sql = "SELECT itemsVenda.desprod,itemsVenda.quantidade,itemsVenda.valorUnitario,itemsVenda.valorTotal FROM itemsVenda WHERE itemsVenda.id = ?";
            PreparedStatement st = (PreparedStatement) con.prepareStatement(sql);
            st.setInt(1, idVenda);
            ResultSet rs = st.executeQuery();
            ResultSetMetaData rsmt = (ResultSetMetaData) rs.getMetaData();
            int numCol = rsmt.getColumnCount();
            while (rs.next()) {
                Object[] vendas = new Object[numCol];
                for (int i = 0; i < numCol; i++) {
                    vendas[i] = rs.getObject(i + 1);
                }
                aModel.addRow(vendas);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }

