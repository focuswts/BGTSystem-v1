    private void buscarVendas() {
        DefaultTableModel aModel = (DefaultTableModel) this.tb_Vendas.getModel();
        try {
            int numlinhas = aModel.getRowCount();
            for (int i = 0; i < numlinhas; i++) {
                aModel.removeRow(0);
            }
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy", Locale.US);
            Date data = formato.parse(Ftxt_searchField.getText());

            java.sql.Date dataSql = new java.sql.Date(data.getTime());
            String sql = "SELECT * from vendas WHERE vendas.data = ?";
            PreparedStatement st = (PreparedStatement) con.prepareStatement(sql);
            st.setDate(1, dataSql);
            ResultSet rs = st.executeQuery();
            ResultSetMetaData rsmt = (ResultSetMetaData) rs.getMetaData();
            int numCol = rsmt.getColumnCount();
            while (rs.next()) {
                Object[] vendas = new Object[numCol];
                for (int i = 0; i < numCol; i++) {
                    vendas[i] = rs.getObject(i + 1);
                }
                aModel.addRow(vendas);
            }

        } catch (Exception e) {
        }

    }

